function metododisparo(a,b,alfa,beta,n,p,q,r)
p=inline(p,'x');
q=inline(q,'x');
r=inline(r,'x');
h=(b-a)/n;
u(1,1)=alfa;
u(2,1)=0;
v(1,1)=0;
v(2,1)=1;
fprintf(' \n\t\t\t MÉTODO DEL DISPARO \n\n');
fprintf('\t\t\t---------------------\n');
fprintf('\t\t\t   x(i)\t    w1(ti)\t\t  \n');
fprintf('\t\t\t---------------------\n');
for i=1:n
    x1=a+(i-1)*h;
    k(1,1)=h*u(2,i);
    k(1,2)=h*(p(x1)*u(2,i)+q(x1)*u(1,i)+r(x1));
    k(2,1)=h*(u(2,i)+(1/2)*k(1,2));
    k(2,2)=h*(p(x1+(h/2))*(u(2,i)+(1/2)*k(1,2))+q(x1+(h/2))*(u(1,i)+(1/2)*k(1,1))+r(x1+(h/2)));
    k(3,1)=h*(u(2,i)+(1/2)*k(2,2));
    k(3,2)=h*(p(x1+(h/2))*(u(2,i)+(k(2,2)/2))+q(x1+(h/2))*(u(1,i)+(k(2,1)/2))+r(x1+(h/2)));
    k(4,1)=h*(u(2,i)+k(3,2));
    k(4,2)=h*(p(x1+h)*(u(2,i)+k(3,2))+q(x1+h)*(u(1,i)+k(3,1))+r(x1+h));
    u(1,i+1)=u(1,i)+(k(1,1)+2*k(2,1)+2*k(3,1)+k(4,1))/6;
    u(2,i+1)=u(2,i)+(k(1,2)+2*k(2,2)+2*k(3,2)+k(4,2))/6;
    %%%%%%%%%%%%%%%%%%%%%
    k1(1,1)=h*v(2,i);
    k1(1,2)=h*(p(x1)*v(2,i)+q(x1)*v(1,i));
    k1(2,1)=h*(v(2,i)+(k1(1,2)/2));
    k1(2,2)=h*(p(x1+(h/2))*(v(2,i)+(k1(1,2)/2))+q(x1+(h/2))*(v(1,i)+(k1(1,1)/2)));
    k1(3,1)=h*(v(2,i)+(k1(2,2)/2));
    k1(3,2)=h*(p(x1+(h/2))*(v(2,i)+(k1(2,2)/2))+q(x1+(h/2))*(v(1,i)+(k1(2,1)/2)));
    k1(4,1)=h*(v(2,i)+k1(3,2));
    k1(4,2)=h*(p(x1+h)*(v(2,i)+k1(3,2))+q(x1+h)*(v(1,i)+k1(3,1)));
    v(1,i+1)=v(1,i)+(k1(1,1)+2*k1(2,1)+2*k1(3,1)+k1(4,1))/6;
    v(2,i+1)=v(2,i)+(k1(1,2)+2*k1(2,2)+2*k1(3,2)+k1(4,2))/6;
    %%%%%%%%%%%%%%%%%%%%%%%%%%%%
    
end
    w(1,1)=alfa;
    w(2,1)=(beta-u(1,n+1))/v(1,n+1);
    fprintf('\t\t\t  %3.2f\t  %3.7f\t\n',a,w(1,1))
    %%%%%%%%%%%%%%%%%%%%%%%%%%%%
    for i=1:n
        W1=u(1,i+1)+w(2,1)*v(1,i+1);
        W2=u(2,i+1)+w(2,1)*v(2,i+1);
        x1=a+i*h;
        fprintf('\t\t\t  %3.2f\t  %3.7f\t\n',x1,W1)
    end
   
    
    
    
        
    
     


